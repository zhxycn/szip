void Crc32::update(const uint8_t* data, size_t size) noexcept {
    for (size_t i = 0; i < size; ++i) {
        state_ = table_[(state_ ^ data[i]) & 0xFF] ^ (state_ >> 8);
    }
}

void Crc32::update(uint8_t byte) noexcept {
    state_ = table_[(state_ ^ byte) & 0xFF] ^ (state_ >> 8);
}

uint32_t Crc32::finalize() const noexcept {
    return state_ ^ 0xFFFFFFFF;
}

void Crc32::reset() noexcept {
    state_ = 0xFFFFFFFF;
}

